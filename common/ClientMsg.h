/**
 * @file    ClientMsg.h
 * @brief  客户端 ↔ 服务器 消息协议定义
 *
 * 包含所有 GameClient 与 GameServer 之间的协议号枚举及消息结构体。
 * 采用定长二进制封包（非 protobuf），协议号编码规则如下：
 *
 * 协议号范围约定：
 * | 范围               | 模块       |
 * |--------------------|-----------|
 * | 0x0001 ~ 0x00FF    | 登录/注册/用户选择 |
 * | 0x0100 ~ 0x01FF    | 场景/移动      |
 * | 0x0200 ~ 0x02FF    | 战斗          |
 * | 0x0300 ~ 0x03FF    | 背包/物品      |
 * | 0x0400 ~ 0x04FF    | 技能          |
 * | 0x0500 ~ 0x05FF    | 聊天          |
 * | 0x0600 ~ 0x06FF    | 社交（好友/队伍/公会） |
 * | 0x0700 ~ 0x07FF    | 任务          |
 * | 0x0F00 ~ 0x0FFF    | 系统/心跳      |
 *
 * 命名约定：
 * - C2S_ 前缀：客户端发往服务器 (Client to Server)
 * - S2C_ 前缀：服务器发往客户端 (Server to Client)
 */

#pragma once
#include <cstdint>

/**
 * @brief 客户端消息协议号枚举
 */
enum class ClientMsgID : uint16_t
{
    // ============================================================
    //  登录 (0x0001 ~ 0x0009)
    // ============================================================
    C2S_LOGIN_REQ        = 0x0001,  /**< C→S: 登录请求 */
    S2C_LOGIN_RSP        = 0x0002,  /**< S→C: 登录响应 */
    C2S_REGISTER_REQ     = 0x0003,  /**< C→S: 注册请求 */
    S2C_REGISTER_RSP     = 0x0004,  /**< S→C: 注册响应 */
    C2S_SELECT_USER_REQ  = 0x0005,  /**< C→S: 选择用户进入游戏 */
    S2C_USER_LIST        = 0x0006,  /**< S→C: 用户列表 */
    C2S_CREATE_USER_REQ  = 0x0007,  /**< C→S: 创建用户 */
    S2C_CREATE_USER_RSP  = 0x0008,  /**< S→C: 创建用户响应 */
    S2C_ENTER_GAME       = 0x0009,  /**< S→C: 通知客户端进入游戏世界 */

    // ============================================================
    //  场景/移动 (0x0101 ~ 0x0107)
    // ============================================================
    C2S_MOVE_REQ         = 0x0101,  /**< C→S: 玩家移动 */
    S2C_MOVE_NOTIFY      = 0x0102,  /**< S→C: 移动同步广播 */
    S2C_ENTER_MAP        = 0x0103,  /**< S→C: 进入地图通知 */
    S2C_LEAVE_MAP        = 0x0104,  /**< S→C: 离开地图通知 */
    S2C_SPAWN_ENTITY     = 0x0105,  /**< S→C: 实体进入视野 */
    S2C_DESPAWN_ENTITY   = 0x0106,  /**< S→C: 实体离开视野 */
    C2S_TELEPORT_REQ     = 0x0107,  /**< C→S: 传送请求 */

    // ============================================================
    //  战斗 (0x0201 ~ 0x0204)
    // ============================================================
    C2S_ATTACK_REQ       = 0x0201,  /**< C→S: 普通攻击 */
    S2C_ATTACK_NOTIFY    = 0x0202,  /**< S→C: 攻击结果广播 */
    S2C_HP_CHANGE        = 0x0203,  /**< S→C: 血量变化通知 */
    S2C_ENTITY_DIE       = 0x0204,  /**< S→C: 实体死亡 */

    // ============================================================
    //  背包/物品 (0x0301 ~ 0x0305)
    // ============================================================
    C2S_BAG_INFO_REQ     = 0x0301,  /**< C→S: 请求背包数据 */
    S2C_BAG_INFO_RSP     = 0x0302,  /**< S→C: 背包数据响应 */
    C2S_USE_ITEM_REQ     = 0x0303,  /**< C→S: 使用物品 */
    S2C_USE_ITEM_RSP     = 0x0304,  /**< S→C: 使用物品结果 */
    C2S_DROP_ITEM_REQ    = 0x0305,  /**< C→S: 丢弃物品 */

    // ============================================================
    //  技能 (0x0401 ~ 0x0402)
    // ============================================================
    C2S_SKILL_REQ        = 0x0401,  /**< C→S: 释放技能 */
    S2C_SKILL_NOTIFY     = 0x0402,  /**< S→C: 技能释放广播 */

    // ============================================================
    //  聊天 (0x0501 ~ 0x0504)
    // ============================================================
    C2S_CHAT_REQ         = 0x0501,  /**< C→S: 发送聊天消息 */
    S2C_CHAT_NOTIFY      = 0x0502,  /**< S→C: 聊天消息广播 */
    C2S_WHISPER_REQ      = 0x0503,  /**< C→S: 私聊 */
    S2C_WHISPER_NOTIFY   = 0x0504,  /**< S→C: 私聊通知 */

    // ============================================================
    //  社交 (0x0601 ~ 0x0611)
    // ============================================================
    C2S_ADD_FRIEND_REQ   = 0x0601,  /**< C→S: 添加好友 */
    S2C_ADD_FRIEND_RSP   = 0x0602,  /**< S→C: 添加好友响应 */
    S2C_FRIEND_LIST      = 0x0603,  /**< S→C: 好友列表 */
    C2S_CREATE_TEAM_REQ  = 0x0610,  /**< C→S: 创建队伍 */
    S2C_TEAM_INFO        = 0x0611,  /**< S→C: 队伍信息 */

    // ============================================================
    //  任务 (0x0701 ~ 0x0704)
    // ============================================================
    C2S_QUEST_ACCEPT_REQ = 0x0701,  /**< C→S: 接取任务 */
    S2C_QUEST_INFO       = 0x0702,  /**< S→C: 任务信息同步 */
    C2S_QUEST_SUBMIT_REQ = 0x0703,  /**< C→S: 提交任务 */
    S2C_QUEST_RESULT     = 0x0704,  /**< S→C: 任务提交结果 */

    // ============================================================
    //  系统 (0x0F01 ~ 0x0F04)
    // ============================================================
    C2S_HEARTBEAT        = 0x0F01,  /**< C→S: 心跳请求 */
    S2C_HEARTBEAT        = 0x0F02,  /**< S→C: 心跳响应 */
    S2C_KICK             = 0x0F03,  /**< S→C: 服务器踢人通知 */
    S2C_NOTICE           = 0x0F04,  /**< S→C: 系统公告 */
};

// ============================================================
//  客户端消息结构体（简化，实际项目可替换为 protobuf/flatbuffers）
// ============================================================
#pragma pack(push, 1)

/**
 * @brief C→S: 登录请求
 *
 * 客户端发送账号密码，由 GatewayServer 转发至 RecordServer 验证。
 */
struct Msg_C2S_LoginReq
{
    char account[32];   /**< 账号（明文，实际应加密） */
    char password[32];  /**< 密码（明文，实际应加密或使用 token） */
};

/**
 * @brief S→C: 登录响应
 *
 * code=0 时 userID 为上次登录用户 ID（0 表示无用户需创建）。
 *
 * 该字段用于客户端登录流程记忆功能：服务器在验证账号密码通过后，
 * 查询该账号上次登录的 userID 并返回给客户端，客户端据此可以直接
 * 选择该用户进入游戏，跳过用户列表界面，提升用户登录体验。
 */
struct Msg_S2C_LoginRsp
{
    int32_t  code;      /**< 错误码：0=成功, 1=账号密码错误, -1=服务器内部错误 */
    char     msg[64];   /**< 可读的错误描述 */
    uint64_t userID;    /**< 上次登录用户 ID（0=无用户） */
};

/**
 * @brief C→S: 玩家移动请求
 *
 * 客户端持续上报位置，SceneServer 通过 AOIServer 广播给视野内玩家。
 */
struct Msg_C2S_MoveReq
{
    uint64_t userID;    /**< 发起移动的用户 ID */
    float    x, y, z;   /**< 目标坐标 */
    float    dir;       /**< 朝向（弧度） */
    uint8_t  moveType;  /**< 移动类型：0=行走 1=跑步 */
};

/**
 * @brief S→C: 移动同步广播
 *
 * 当周围玩家移动时，服务器将此消息广播给视野内所有玩家。
 */
struct Msg_S2C_MoveNotify
{
    uint64_t userID;    /**< 移动的用户 ID */
    float    x, y, z;   /**< 目标坐标 */
    float    dir;       /**< 朝向 */
    uint8_t  moveType;  /**< 移动类型 */
};

/**
 * @brief C→S: 聊天消息
 */
struct Msg_C2S_Chat
{
    uint8_t  channel;       /**< 频道：0=世界 1=区域 2=队伍 3=公会 */
    char     content[256];  /**< 聊天内容（UTF-8） */
};

/**
 * @brief S→C: 聊天广播
 */
struct Msg_S2C_Chat
{
    uint64_t fromID;        /**< 发送者用户 ID */
    char     fromName[32];  /**< 发送者用户名 */
    uint8_t  channel;       /**< 频道 */
    char     content[256];  /**< 聊天内容 */
};

/**
 * @brief C→S: 心跳请求
 *
 * 客户端每 10 秒发送一次，超时 60 秒未收到则服务器断开连接。
 */
struct Msg_C2S_Heartbeat
{
    uint32_t seq;  /**< 序列号，响应时原样返回 */
};

/**
 * @brief S→C: 心跳响应
 */
struct Msg_S2C_Heartbeat
{
    uint32_t seq;        /**< 回显客户端序列号 */
    uint64_t serverTime; /**< 服务器当前时间（毫秒时间戳） */
};

/**
 * @brief S→C: 进入游戏世界
 *
 * 当客户端选择用户进入游戏后，服务器按以下流程处理并下发此消息：
 *
 * 1. 客户端发送选择用户请求（用户点击"进入游戏"按钮）；
 * 2. SceneServer 接收请求，从数据库加载该用户的完整数据
 *    （等级、血量、魔法、道具、装备、技能、任务进度等）；
 * 3. SceneServer 将用户加载到指定场景地图，初始化 AOI（Area of Interest）视野；
 * 4. SceneServer 下发 Msg_S2C_EnterGame 通知客户端进入游戏世界，
 *    携带用户基本信息（userID、名称、坐标、等级、HP/MP 等）；
 * 5. 客户端收到此消息后，加载对应地图资源、初始化 UI、
 *    并在场景中创建本地用户实体，完成从登录界面到游戏场景的切换。
 *
 * 注意：此消息后通常紧接 Msg_S2C_SpawnEntity 批量下发视野内的其他实体。
 */
struct Msg_S2C_EnterGame
{
    uint64_t userID;
    char     name[32];
    uint32_t mapID;
    float    x, y, z;
    uint32_t level;
    uint32_t hp;
    uint32_t maxHP;
    uint32_t mp;
    uint32_t maxMP;
};

/**
 * @brief S→C: 实体进入视野
 *
 * 当玩家进入某个实体（其他玩家、NPC、怪物等）的 AOI 视野范围时，
 * 服务器下发此消息通知客户端创建并渲染该实体。
 *
 * entityType 枚举：
 * | 值 | 类型   | 说明                                         |
 * |----|--------|----------------------------------------------|
 * | 0  | PLAYER | 其他在线玩家，客户端应加载其外观、装备等数据 |
 * | 1  | NPC    | 非玩家角色（商人、任务NPC、传送员等），不可攻击 |
 * | 2  | MONSTER| 野外怪物，可攻击、会掉落物品                     |
 * | 3  | PET    | 玩家宠物/召唤物，跟随主人行动                 |
 * | 4  | ITEM   | 掉落在地面上的物品实体，可拾取                 |
 */
struct Msg_S2C_SpawnEntity
{
    uint64_t entityID;
    char     name[32];
    uint32_t level;
    float    x, y, z;
    float    dir;
    uint8_t  entityType;  /**< 实体类型，参见枚举说明 */
};

/**
 * @brief S→C: 实体离开视野
 *
 * 服务器在以下时机下发此消息，通知客户端移除指定实体：
 *
 * 1. **离开AOI范围**：当某实体移出当前玩家的视野范围（超出 AOI 半径）时；
 * 2. **实体下线**：当其他玩家断开连接、登出游戏时；
 * 3. **实体死亡消失**：怪物被击杀后尸体消失、限时NPC活动结束消失时；
 * 4. **传送/切换地图**：当实体通过传送门或进入副本离开当前场景时；
 * 5. **实体销毁**：临时召唤物到期、任务道具被拾取后消失等场景。
 *
 * 客户端收到此消息后应立即销毁该实体的本地渲染对象，
 * 并从实体管理器中移除，同时清理相关 UI 引用（如血条、名字等）。
 */
struct Msg_S2C_DespawnEntity
{
    uint64_t entityID;
};

