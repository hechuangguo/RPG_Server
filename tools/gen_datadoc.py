def main() -> int:
    parser = argparse.ArgumentParser(description="DataDoc Excel -> database Lua")
    parser.add_argument(
        "--init",
        action="store_true",
        help="生成示例 Common/DataDoc/*.xlsx（不执行转换）",
    )
    args = parser.parse_args()

    if args.init:
        initSampleWorkbooks()
        return 0

    if not DATA_DOC_DIR.is_dir():
        print(f"Common/DataDoc 目录不存在: {DATA_DOC_DIR}", file=sys.stderr)
        return 1

    xlsxFiles = sorted(DATA_DOC_DIR.glob("*.xlsx"))
    if not xlsxFiles:
        print("Common/DataDoc 下无 .xlsx，执行 --init 创建示例表")
        initSampleWorkbooks()
        xlsxFiles = sorted(DATA_DOC_DIR.glob("*.xlsx"))

    ok = 0
    for path in xlsxFiles:
        if path.name.startswith("~$"):
            continue
        try:
            out = convertWorkbook(path)
            print(f"[ok] {path.name} -> {out.relative_to(ROOT)}")
            ok += 1
        except Exception as exc:
            print(f"[fail] {path.name}: {exc}", file=sys.stderr)

    if ok == 0:
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
